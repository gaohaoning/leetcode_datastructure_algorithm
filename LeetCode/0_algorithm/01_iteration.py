"""
斐波那契数列
"""


def fibonacci(n):
    if n < 0:
        return -1
    elif n <= 1:
        return 1
    else:
        a, b = 1, 1
        c = 0
        for i in range(n - 1):
            c = a + b
            a = b
            b = c
            pass
        return c

