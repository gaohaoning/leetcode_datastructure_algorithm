"""
思路:
参考 LeetCode33，
思路:
    1.寻找旋转点（pole）（最小值）
    2.旋转点左右分别做二分查找
不同点:
    由于存在重复元素，所以在进行元素值比较时需要:
        1.比较 left 和 right 时，如果值相等，将 left 右移至与 right 不等的值;
        2.比较 mid 和 right 时，如果值相等，将 right 左移至 mid 与 right 不等;
"""
class Solution(object):
    def binary_search(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: int
        """
        left, right = 0, len(nums) - 1
        while left <= right:
            mid = (left + right)/2
            if nums[mid] == target:
                return mid
            elif nums[mid] < target:
                left = mid + 1
            elif nums[mid] > target:
                right = mid - 1
            pass
        return -1

    def search(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: int
        """
        left, right = 0, len(nums) - 1
        while left < right:
            # !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
            # 在正式比较之前，先移动左指针，使得它指向一个和右指针不同的数字上。
            if nums[left] == nums[right]:
                left += 1
                continue
            # !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
            mid = (left + right)/2
            if nums[mid] == nums[right]:
                # !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
                # 如果 mid 和 right 值相等，则 right 左移直至 mid 和 right 不等。
                right -= 1
                continue
                # !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
            elif nums[mid] > nums[right]:
                left = mid + 1
            elif nums[mid] < nums[right]:
                right = mid
                pass
            pass
        pole = right
        # print 'pole is %s' % pole
        #
        ret = self.binary_search(nums[:pole], target)
        if ret == -1:
            ret = self.binary_search(nums[pole:], target)
            if ret != -1:
                ret += + len(nums[:pole])
            pass
        return ret != -1
